package practicejava;

public class Concatenation {

	// Combining 2 string information using plus operator is known as Concatenation
	public static void main(String[] args) {
		
		
		
		System.out.println("Hello"+"hi");
		
		System.out.println(25 + "Products");
		
		System.out.println(25+25+"Hi");
		//50hi
		
		System.out.println("Hi"+25+25);
		//Hi2525
		
		System.out.println("Hi"+25+25+25);
		//Hi252525
		
		System.out.println("10"+"10");
		
		System.out.println(25+25+"Hi"+30+30+30+30);
		//50Hi30303030
		
		System.out.println("25"+25);
		
		System.out.println("Hi"+(25+25));
		
	}
}
