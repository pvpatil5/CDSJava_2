package com.casting;

public class Demo {

	// A process of converting one data type to another -->Type Casting
	// up casting & down casting--> Object casting
	
	public static void main(String[] args) 
	{
	
		int a =10; // 4 bytes
		
		System.out.println(a);
		 
		double b=20;  // 8 byte
	
	
		a=(int) b;
		
		System.out.println(a+b);
		
		
	}
}
